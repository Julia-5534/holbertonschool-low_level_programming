/**
 * op_add - adds numbers
 * @a: integer
 * @b: integer
 * Return: result
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts numbers
 * @a: integer
 * @b: integer
 * Return: result
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies numbers
 * @a: integer
 * @b: integer
 * Return: result
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides numbers
 * @a: integer
 * @b: integer
 * Return: result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainders numbers
 * @a: integer
 * @b: integer
 * Return: result
 */

int op_mod(int a, int b)
{
	return (a % b);
}
